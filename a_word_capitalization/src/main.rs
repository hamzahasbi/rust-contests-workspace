type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &PreCalc) {
    let mut s: Vec<char> = input.read_line().chars().collect();
    s[0] = s[0].to_ascii_uppercase();
    out.print_line(s.iter().collect::<String>());
}

